// Contact Page JavaScript
document.addEventListener('DOMContentLoaded', function() {
    
    // Form submission
    document.getElementById('contact-form').addEventListener('submit', function(e) {
        e.preventDefault();
        
        const submitBtn = document.querySelector('.btn-submit');
        const originalText = submitBtn.innerHTML;
        
        // Validate required fields
        const requiredFields = this.querySelectorAll('[required]');
        let isValid = true;
        
        requiredFields.forEach(field => {
            if (!field.value.trim()) {
                field.classList.add('error');
                isValid = false;
            } else {
                field.classList.remove('error');
            }
        });
        
        if (!isValid) {
            alert('Please fill in all required fields.');
            return;
        }
        
        submitBtn.innerHTML = '<span>Sending...</span><span class="btn-icon">⏳</span>';
        submitBtn.disabled = true;
        
        // Simulate form submission
        setTimeout(() => {
            document.querySelector('.contact-form-fancy').innerHTML = `
                <div class="form-success">
                    <div class="success-icon">✅</div>
                    <h3>Request Sent Successfully!</h3>
                    <p>Thank you for your interest in business blueprints! We'll get back to you within 24 hours with personalized recommendations.</p>
                    <div class="success-details">
                        <p><strong>Reference Number:</strong> #${Math.random().toString(36).substr(2, 9).toUpperCase()}</p>
                        <p><strong>Next Steps:</strong> Check your email for our blueprint questionnaire</p>
                    </div>
                    <button onclick="location.reload()" class="btn-primary-fancy">
                        <span>Send Another Message</span>
                        <i class="arrow-right">→</i>
                    </button>
                </div>
            `;
        }, 2000);
    });
    
    // Live chat functionality
    const chatToggle = document.getElementById('chatToggle');
    const chatWindow = document.getElementById('chatWindow');
    const chatClose = document.getElementById('chatClose');
    const chatInput = document.getElementById('chatInput');
    const sendBtn = document.getElementById('sendBtn');
    
    if (chatToggle && chatWindow) {
        chatToggle.addEventListener('click', function() {
            chatWindow.classList.toggle('active');
            document.getElementById('chatNotification').style.display = 'none';
        });
    }
    
    if (chatClose) {
        chatClose.addEventListener('click', function() {
            chatWindow.classList.remove('active');
        });
    }
    
    // Quick options in chat
    document.querySelectorAll('.quick-option').forEach(option => {
        option.addEventListener('click', function() {
            const message = this.dataset.message;
            addMessage(message, 'user');
            
            // Simulate auto-response based on the message
            setTimeout(() => {
                let response = 'Thanks for your interest! ';
                
                if (message.includes('online')) {
                    response += 'Our online business blueprints are perfect for beginners. They typically require $500-$5,000 to start. Would you like specific recommendations?';
                } else if (message.includes('service')) {
                    response += 'Service-based businesses are great for immediate income. We have 20 different service blueprints. What skills do you have?';
                } else if (message.includes('low-investment')) {
                    response += 'We have excellent low-investment options starting under $1,000. Let me know your budget and interests!';
                } else {
                    response += 'I\'d be happy to help you find the perfect business blueprint. What\'s your experience level and budget range?';
                }
                
                addMessage(response, 'agent');
            }, 1000);
        });
    });
    
    // Send message functionality
    function addMessage(text, sender) {
        const messagesContainer = document.getElementById('chatMessages');
        if (!messagesContainer) return;
        
        const messageDiv = document.createElement('div');
        messageDiv.className = `chat-message ${sender}-message`;
        
        messageDiv.innerHTML = `
            <div class="message-avatar">${sender === 'agent' ? '👨‍💼' : '👤'}</div>
            <div class="message-content">
                <div class="message-text">${text}</div>
                <div class="message-time">${new Date().toLocaleTimeString()}</div>
            </div>
        `;
        
        messagesContainer.appendChild(messageDiv);
        messagesContainer.scrollTop = messagesContainer.scrollHeight;
    }
    
    // Send button functionality
    if (sendBtn && chatInput) {
        sendBtn.addEventListener('click', function() {
            const message = chatInput.value.trim();
            if (message) {
                addMessage(message, 'user');
                chatInput.value = '';
                
                // Simulate response
                setTimeout(() => {
                    let response = 'Thank you for your message. ';
                    
                    // Simple keyword-based responses
                    if (message.toLowerCase().includes('price') || message.toLowerCase().includes('cost')) {
                        response += 'Our business blueprints range from $500 to $50,000 startup costs. Each blueprint includes detailed cost breakdowns and ROI projections.';
                    } else if (message.toLowerCase().includes('time') || message.toLowerCase().includes('how long')) {
                        response += 'Most of our blueprints can be launched within 30-90 days, depending on the complexity and your available time.';
                    } else if (message.toLowerCase().includes('help') || message.toLowerCase().includes('support')) {
                        response += 'We provide comprehensive support including step-by-step guides, cost analysis, marketing strategies, and ongoing consultation.';
                    } else {
                        response += 'I\'ll connect you with one of our blueprint specialists who can provide detailed information about your specific interests.';
                    }
                    
                    addMessage(response, 'agent');
                }, 1500);
            }
        });
        
        chatInput.addEventListener('keypress', function(e) {
            if (e.key === 'Enter') {
                sendBtn.click();
            }
        });
    }
    
    // Real-time validation
    document.querySelectorAll('input, select, textarea').forEach(field => {
        field.addEventListener('blur', function() {
            if (this.hasAttribute('required') && !this.value.trim()) {
                this.classList.add('error');
            } else {
                this.classList.remove('error');
            }
        });
    });
    
    // Animate numbers on scroll
    const statNumbers = document.querySelectorAll('.stat-number[data-count]');
    
    const animateNumbers = () => {
        statNumbers.forEach(stat => {
            const target = parseInt(stat.getAttribute('data-count'));
            const current = parseInt(stat.textContent);
            const increment = target / 100;
            
            if (current < target) {
                stat.textContent = Math.ceil(current + increment);
                setTimeout(animateNumbers, 20);
            } else {
                stat.textContent = target;
            }
        });
    };
    
    // Trigger animation when stats are visible
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                animateNumbers();
                observer.unobserve(entry.target);
            }
        });
    });
    
    const statsSection = document.querySelector('.hero-stats');
    if (statsSection) {
        observer.observe(statsSection);
    }
});
</script>

