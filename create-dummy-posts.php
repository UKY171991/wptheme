// Function to create a dummy post
function create_dummy_post($post_data) {
    // Check if post with this title already exists
    $existing_post = get_page_by_title($post_data['title'], OBJECT, 'post');
    if ($existing_post) {
        return $existing_post->ID;
    }

    // Create the post
    $post_args = array(
        'post_title'    => $post_data['title'],
        'post_content'  => $post_data['content'],
        'post_excerpt'  => $post_data['excerpt'],
        'post_status'   => 'publish',
        'post_type'     => 'post',
        'post_author'   => 1,
        'post_date'     => date('Y-m-d H:i:s', strtotime('-' . rand(1, 30) . ' days')),
    );

    $post_id = wp_insert_post($post_args);

    if ($post_id) {
        // Create category if it doesn't exist
        $category = wp_create_category($post_data['category']);
        wp_set_post_categories($post_id, array($category));

        // Add tags
        wp_set_post_tags($post_id, $post_data['tags']);

        // Set featured image (placeholder)
        if (isset($post_data['featured_image'])) {
            // For now, we'll store the image URL as a custom field
            // In a real implementation, you'd download and attach the image
            update_post_meta($post_id, '_thumbnail_url', $post_data['featured_image']);
        }

        return $post_id;
    }

    return false;
}

